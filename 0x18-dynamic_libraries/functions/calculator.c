/**
 * add - returns the sum of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of two integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - returns the difference of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of two integers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - returns the product of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product of two integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - returns the quotient of the division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient of the division of two integers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns the remainder of the division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient of the remainder of two integers
 */
int mod(int a, int b)
{
	return (a % b);
}
