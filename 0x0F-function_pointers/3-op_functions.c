#include "3-calc.h"

/**
 * op_add - a function that adds two numbers
 * @a: an integer
 * @b: an integer
 *
 * Return: the sum of the two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that subtracts two numbers
 * @a: an integer
 * @b: an integer
 *
 * Return: the difference of the two integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - a function that multiplies two numbers
 * @a: an integer
 * @b: an integer
 *
 * Return: the product of the two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - a function that adds two numbers
 * @a: an integer
 * @b: an integer
 *
 * Return: the result of the division of the two integers
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - a function that adds two numbers
 * @a: an integer
 * @b: an integer
 *
 * Return: the remainder of division of the two integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
